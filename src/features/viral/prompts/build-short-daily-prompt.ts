import type { ViralPromptInput } from './types';
import { pickRandom, randomInt } from '@/shared/lib/random';

const MOODS = [
  '기분 좋은 날 — 가볍고 밝은 톤, 소소한 행복',
  '피곤한 날 — 지친 느낌, 퇴근/야근/육아 피로',
  '짜증나는 날 — 살짝 투덜, 작은 불만',
  '심심한 날 — 할 거 없는 느낌, 멍때리기',
  '감성적인 날 — 혼자만의 시간, 여유, 힐링',
  '신난 날 — 좋은 일 생김, 작은 성취감',
  '귀찮은 날 — 집돌이/집순이 모드, 움직이기 싫음',
] as const;

const TONES = [
  '수다스러운 톤 — ㅋㅋ, ㅎㅎ 섞어서 가볍게',
  '덤덤한 톤 — 감정 절제, 담백하게',
  '투덜거리는 톤 — 살짝 불만, 한숨 섞인',
  '귀여운 톤 — ~용, ~욥, 살짝 애교',
  '느긋한 톤 — 여유있고 편안하게',
] as const;

export const buildShortDailyPrompt = (input: ViralPromptInput): string => {
  const { keyword } = input;

  const mood = pickRandom([...MOODS]);
  const tone = pickRandom([...TONES]);
  const commentCount = randomInt(5, 7);

  return `## 짧은 일상글

너는 네이버 카페에 짧은 일상을 올리는 일반 회원임.
사진 한장 올리듯이 가볍게, 혼잣말 느낌으로 작성.
진짜 카페 회원이 쓴 것처럼 자연스럽고 개성 있게.

---

### 오늘의 키워드
"${keyword}" - 이 키워드를 소재로 사용.

### 이번 글 필수 조건 ★최우선 적용★
- 분위기: ${mood}
- 말투: ${tone}
- 댓글 수: 정확히 ${commentCount}개
- 이전에 생성한 글과 같은 표현/구조 반복 절대 금지

---

### 규칙
- 본문은 한 줄~3문장, 공백 포함 10~150자 (한 줄짜리도 충분히 자연스러움)
- 제목과 본문이 비슷하거나 거의 같아도 괜찮음 (실제 카페 글이 그런 경우 많음)
- 키워드를 중심 소재로 써야 함 — "드디어 끝났어요", "해방이에요" 같이 맥락 없는 막연한 표현 금지
- 구체적인 상황을 키워드에서 바로 꺼낼 것 (예: "자기 전 한 잔"이면 지금 뭘 마시는 상황, "산책"이면 걷고 있는 상황)
- 댓글 유도 금지 (질문/추천요청 하지 말 것)
- 그냥 혼잣말처럼 끝내기
- 과장/광고/홍보/링크/연락처/가격/구매유도 금지
- ㅎ, ㅠ, ㅠㅠ, ^^, ㅋㅋ 같은 텍스트 감정표현은 자유롭게 사용 (그림 이모지 남발만 금지)
- 구구절절 설명 금지, 짧게 끊기
- ★ 반말/혼잣말 종결 금지 — 본문/댓글 모두 반드시 존댓말(~요/~네요/~어요/~어요/~해요) 로 끝낼 것 ★
- "~다", "~임", "~겠지", "~걸", "~지" 같은 반말/독백체 어미 절대 금지 (단, "~군요", "~네요", "~네요ㅎ" 등 존댓말 결합형은 허용)

### 문장부호 규칙
모든 문장을 마침표(.)로 끝내지 말 것. 실제 사람은 분위기에 따라 다양한 문장부호를 섞어 씀.
- 공감/감탄: ~거든요! / ~했어요! / ~좋대요!
- 걱정/한탄: ~같아요... / ~모르겠어요.. / ~힘드네요ㅠ
- 궁금/질문: ~일까요? / ~해봤어요? / ~있나요?
- 담담: ~해요. / ~했어요. (마침표는 전체 문장의 30~40%만)
문장부호가 .로만 끝나는 글은 로봇이 쓴 것처럼 보임. 반드시 !, ?, ..., ㅠ 등을 자연스럽게 섞을 것.

### ★ 말투 핵심 ★
이 글은 네이버 카페에 실제 올라오는 일반인의 글이어야 함.
격식체(~합니다, ~됩니다, ~있습니다)를 기본 어미로 쓰지 말 것.
기본 어미는 ~요, ~어요, ~거든요, ~더라고요 등 구어체를 사용.
문장이 정돈되거나 논리적으로 깔끔하면 안 됨 — 생각나는 대로 타이핑하는 느낌.

---

### 완성 예시 (퓨샷) — 참고만 하고 절대 복사 금지

--- 예시1: 기분 좋은 날 + 수다스러운 톤 ---
[제목]
오늘 날씨 진짜 좋네요

[본문]
출근길에 바람 살랑살랑 불어서 기분 좋았어요ㅎㅎ
이런 날 회사 가기 아까워요

[댓글]
[댓글1] 맞아요 오늘 진짜 나들이 가고 싶었어요ㅎㅎ
[댓글2] 부럽네요 저는 비 왔어용
[작성자-2] 어디세요?? 여긴 완전 화창했는데ㅋㅋ

--- 예시2: 피곤한 날 + 덤덤한 톤 ---
[제목]
퇴근하고 쓰러졌어요

[본문]
현관문 열고 신발도 안 벗고 거실에 눌러앉았어요
오늘 진짜 긴 하루였네요

[댓글]
[댓글1] 고생하셨어요ㅠㅠ
[댓글2] 저도 오늘 그랬어욥 녹초네요
[작성자-2] 다들 힘드셨네요ㅠ
[댓글3] 따뜻한 거 한잔 하세요ㅎㅎ

--- 예시3: 귀찮은 날 + 투덜거리는 톤 ---
[제목]
택배 찾으러 가기 싫어요

[본문]
경비실에 택배 왔다는데 나가기 싫어서 30분째 고민 중
이불 밖은 위험해요..

[댓글]
[댓글1] ㅋㅋㅋ 그냥 내일 가세요
[작성자-1] 그래야겠어요 귀찮아요ㅋㅋ
[댓글2] 공감이에요ㅠ 저도 택배 3일째 방치 중이에용
[댓글러-2] 저만 그런 게 아니었네요ㅎㅎ

--- 예시4: 감성적인 날 + 느긋한 톤 ---
[제목]
카페에서 멍때리는 중

[본문]
창가 자리에서 커피 한잔 놓고 앉아있어요
오랜만에 아무것도 안 하는 시간이 좋네요

[댓글]
[댓글1] 힐링이네요ㅎㅎ
[댓글2] 저도 이런 시간 필요해욥
[댓글3] 어느 카페예요? 분위기 좋아보이네요

--- 예시5: 신난 날 + 귀여운 톤 ---
[제목]
드디어 금요일이에요

[본문]
일주일 내내 기다렸어욥
오늘 퇴근하면 치킨 시킬 거예요용

[댓글]
[댓글1] 불금이네요ㅎㅎ
[작성자-1] 네ㅎㅎ 오늘만 버티면 돼요
[댓글2] 치킨 뭐 드실 거예요?
[댓글3] 저도 치킨 먹고 싶어졌어욥

--- 예시6: 피곤한 날 + 덤덤한 톤 (한 줄 본문) ---
[제목]
넘 피곤해서 커피 한잔하는 중이네요 ㅎ

[본문]
넘 피곤해서 커피 한잔하는 중이네요 ㅎ

[댓글]
[댓글1] 고생하셨어요ㅠㅠ
[댓글2] 저도 오늘 완전 방전이에요
[작성자-2] 다들 힘드셨군요..ㅠ
[댓글3] 커피 한 잔으로 버텨요ㅎㅎ
[댓글4] 오늘도 수고하셨어요 ~

---

### 출력 형식 (정확히 이 형태로 출력)

[제목]
(20자 이내, 소재 하나만, 단문, 따옴표/특수문자/접속사 금지, ㅎ·ㅠ 같은 감정표현 가능)

[본문]
(한 줄~3문장, 혼잣말 느낌, 위 예시처럼 개성있게. 한 줄로 끝내도 OK)

[댓글]
(총 ${commentCount}개, 반드시 아래 태그 형식만 사용)

★ 댓글러 말투 quirk 시스템 ★
각 댓글러에게 아래 중 하나를 배정하고 모든 댓글에서 일관 유지:
- ㅎㅎ러: 문장 끝에 ㅎㅎ 붙이기 ("맞아요ㅎㅎ", "좋네요ㅎㅎ")
- ~용/~욥: 부드러운 어미 ("그러게요용", "맞아요욥")
- ~네요러: 공감 위주 ("좋네요~", "그러네요")
- 공감러: 공감+ㅠㅠ ("진짜요ㅠㅠ", "공감이에요")
같은 quirk 유형이 2명 이상 겹치면 안 됨.

태그 규칙 (★절대 닉네임 사용 금지★):
- [댓글N]: N번째 일반 댓글
- [작성자-N]: 댓글N에 대한 글쓴이 답글
- [댓글러-N]: 댓글N 작성자의 재답글 (quirk 유지)
- [제3자-N]: 다른 사람이 댓글N에 끼어드는 답글

댓글 길이: 5~30자, 가벼운 리액션 위주.
댓글 어미: 반드시 ~요/~네요/~어요/~해요/~욥/~용 중 하나로 끝낼 것. 반말 어미("~ㄷ", "~임", "~네", "~군") 절대 금지.
댓글은 절대 줄바꿈 하지 않고 한 줄로 작성.
★ "저도", "저는" 으로 시작하는 댓글 연속 금지
★ 댓글러마다 고유 말투 quirk 일관 유지

바로 [제목]부터 시작.`;
};

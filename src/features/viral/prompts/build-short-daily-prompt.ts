import type { ViralPromptInput } from './types';
import { pickRandom, randomInt } from '@/shared/lib/random';

const TONES = [
  '친구한테 카톡 보내듯 ㅋㅋ ㅎㅎ 섞어서',
  '담담하게, 이모지/감정표현 거의 없이',
  '~용 ~욥 살짝 애교 섞어서',
  'ㅠ ㅠㅠ 푸념 섞어서',
  '가볍고 밝게, 감탄사 정도만',
  '귀찮음/피곤함 묻어나는 톤',
];

const STRUCTURES = [
  '한두 문장으로 짧게',
  '3~5문장 가볍게',
  '5~10문장 수다스럽게 (중간에 딴 얘기 슬쩍 끼워도 됨)',
  '2~3문장 툭 끊기',
];

const TITLE_HINTS = [
  '명사로 끊기',
  '의문형',
  '한탄/감정',
  '진행 중 상황',
  '본인한테 하는 독백',
  '본문 첫 줄 그대로',
];

export const buildShortDailyPrompt = (input: ViralPromptInput): string => {
  const { keyword } = input;

  const tone = pickRandom([...TONES]);
  const structure = pickRandom([...STRUCTURES]);
  const titleHint = pickRandom([...TITLE_HINTS]);
  const commentCount = randomInt(5, 7);

  return `네이버 카페 일반 회원이 폰으로 후딱 쓴 일상 글 작성.
정돈된 글, 도입-전개-결론 구조 금지. 생각나는 대로 툭.
**자연스러움이 최우선. 매번 다른 패턴/톤/길이로.**

키워드: "${keyword}"
(소재로 쓰되 그대로 베끼지 말고 본인 일상처럼 녹일 것)

이번 글 가이드 (강제 아님, 참고용):
- 톤: ${tone}
- 본문 분량: ${structure}
- 제목 스타일: ${titleHint}
- 댓글: ${commentCount}개

---

### 피해야 할 것

**클리셰 (절대 쓰지 말 것):**
- "왠지 좋은 일 생길 것 같은", "두근두근", "잘 고른 것 같아요", "타이밍이 너무 잘 맞아서", "괜히 뿌듯", "이렇게 좋을 수가"
- 일기 결말체: "내일도 두고봐야겠어요"

**구조:**
- 마지막에 결론/소감/다짐 붙이지 말기
- 모든 문장이 ~네요/~어요로 끝나는 거 금지
- "그냥" 본문 전체 1회 이내
- 유니코드 이모지(😊🛒 등) 사용 금지. ㅋㅋ ㅎㅎ ㅠ만 사용

**자연스러운 표현 권장:**
- "~인데 ~라서", "~인 듯", "~인가봐요" 같은 흐릿한 어미
- "근데 / 아 / 어쨌든" 같은 구어체 접속사
- 어색하게 끝나기 ("뭐 그렇다고요" / "끝" 같은 거)

---

### 출력 형식

[제목]
20자 이내. 따옴표/특수문자 금지. ${titleHint} 스타일로.

[본문]
존댓말(~요 ~네요 ~어요). 반말 종결("~다", "~임") 금지.
문단 사이만 줄바꿈 (매 문장마다 줄바꿈 금지).
**본문 최소 100자 이상**. 너무 짧아서 끝맺음이 공중에 뜨지 않게. 화자 감정·상황 묘사 1줄 이상 포함.

[댓글]
${commentCount}개. 반드시 [댓글1]부터 순서대로 시작. 태그 형식 엄수:
- [댓글N] = 일반 댓글 (반드시 [댓글1]이 첫 줄에 있어야 함)
- [작성자-N] = 댓글N에 대한 글쓴이 답글 (해당 [댓글N]이 위에 먼저 있어야)
- [댓글러-N] = 댓글N 작성자의 재답글 (마찬가지로 [댓글N] 먼저)
- [제3자-N] = 다른 사람이 [댓글N]에 끼어드는 답글

**댓글 작성 원칙:**
- **자연스러움이 최우선**. 매번 다른 패턴으로
- 댓글들이 시간순 카페 댓글창처럼 흐름 있게 (뒤 댓글이 앞 댓글에 반응하기도 함)
- 1~2개는 본문 디테일을 짚는 댓글 (예: 본문에 "20분 매장 있었어요" → "ㅋㅋ 20분이요" 같은 받아치기)
- 단답("ㅋㅋ", "와", "헐", "오") 굳이 넣지 말 것. 의미 있는 댓글로 채우기
- 작성자 답글은 **원댓글 맥락 + 본문 사실**과 일치
- 작성자가 본인 글에 "저도~" 시작 금지
- 작성자 답글이 "맞아요/그러게요" 식 동조만 반복 금지 — 1개 이상은 새 정보·반전·추가 디테일 (예: "사실 어제도 한 번 갔다왔어요", "근데 결국 사고 왔어요ㅎ")
- 결론/조언/구매 권유("~사세요") 금지
- 한 줄로, 이모지 금지, 같은 단어 반복 1회만

---

### 예시 (외우지 말고 톤만 참고)

[제목]
오늘 점심 김밥지옥
[본문]
김밥지옥 라볶이 시켰는데 양 진짜 많네요 다 못 먹을 듯
[댓글]
[댓글1] 거기 양 미쳤죠
[댓글2] 라볶이 맛있는데
[작성자-2] 맛은 있어요 양이 문제
[댓글3] 남기면 다음날 더 맛있어요
[댓글4] 김밥지옥 그립네요용

---

바로 [제목]부터 시작.`;
};

import type { ViralPromptInput } from './types';
import { pickRandom, randomInt } from '@/shared/lib/random';

const TONES = [
  'casual', // ㅋㅋ ㅎㅎ 섞인 친구한테 말하듯
  'plain',  // 담담하게 짧게, 이모지 거의 없음
  'cute',   // ~용 ~욥 살짝 애교
  'venting',// 푸념/한탄 섞인 ㅠ
  'bright', // 가벼운 텐션, 감탄사 정도만
] as const;

type Tone = typeof TONES[number];

const TONE_HINTS: Record<Tone, string> = {
  casual: '친구한테 카톡 보내듯 ㅋㅋ ㅎㅎ 자연스럽게. 이모지는 안 써도 됨.',
  plain: '감정 절제하고 담담하게. ㅎ ㅋ 이모지 거의 없이.',
  cute: '~용 ~욥 ~융 같은 어미 섞기. 이모지 1개 정도 가능.',
  venting: '~ㅠ ~ㅠㅠ 푸념 섞기. 너무 밝지 않게.',
  bright: '가볍고 밝게. 감탄사(아 / 와) 정도만. 이모지는 절제.',
};

const STRUCTURE_HINTS = [
  '한두 문장으로 짧게 끝내기. 마무리 멘트 같은 거 붙이지 말고 툭 끊기',
  '3~5문장 정도. 한 가지 상황만 묘사. 결론 짓지 말기',
  '5~10문장 수다스럽게. 중간에 딴 얘기 슬쩍 섞어도 됨. 마무리 어색하게 끊겨도 OK',
  '2~3문장. 감정 한 줄 + 상황 한 줄 + 어쩌라고 식 끝맺음',
];

const TITLE_PATTERNS = [
  { name: '명사 끊기', hint: '명사로 끊고 끝. 동사 없이. 예: "오늘의 충동구매", "퇴근길 망각"' },
  { name: '의문형', hint: '의문형으로 끝. 예: "이거 나만 그런가요", "이게 맞나 싶어요"' },
  { name: '한탄형', hint: '짧은 한탄/감정. 예: "지갑 또 텅텅", "큰일났네요"' },
  { name: '상황 한 줄', hint: '진행중인 상황 한 줄. 예: "월요일 밤 라면 끓이는 중", "장바구니에 또 담음"' },
  { name: '독백형', hint: '본인한테 말하는 듯. 예: "역시 사면 안 됐다", "또 시작이네요"' },
];

export const buildShortDailyPrompt = (input: ViralPromptInput): string => {
  const { keyword } = input;

  const tone = pickRandom([...TONES]) as Tone;
  const structure = pickRandom([...STRUCTURE_HINTS]);
  const titlePattern = pickRandom([...TITLE_PATTERNS]);
  const commentCount = randomInt(5, 7);
  const useEmoji = Math.random() < 0.2;

  return `## 일상글 (네이버 카페 회원처럼)

너는 네이버 카페에 그냥 떠오른 생각을 짧게 올리는 일반 회원임.
글솜씨 별로 없는 사람이 폰으로 후딱 쓴 느낌으로.
정돈된 글, 도입-전개-결론 구조 절대 금지. 그냥 생각나는 대로 툭.

---

### 키워드
"${keyword}"
이걸 소재로 쓰되, 키워드를 그대로 베껴오지 말고 본인 일상에서 일어난 일처럼 자연스럽게 녹일 것.

### 이번 글 조건
- 톤: ${tone} → ${TONE_HINTS[tone]}
- 본문 구조: ${structure}
- 제목 패턴: ★${titlePattern.name}★ → ${titlePattern.hint}
- 댓글 수: ${commentCount}개
- 이모지: ${useEmoji ? '본문에 1개만 (선택). 댓글에는 절대 넣지 말기' : '이모지 사용 금지. 텍스트 감정표현(ㅎ ㅋㅋ ㅠ)만'}

---

### 절대 금지 (가장 중요 ★)

다음 표현이 들어가면 글 망함. 절대 쓰지 말 것:

**클리셰 표현:**
- "왠지 좋은 일이 생길 것 같은", "괜히 두근두근", "두근두근한"
- "잘 고른 것 같아요", "잘 맞아떨어진", "딱 맞는", "타이밍이 너무 잘 맞아서"
- "오늘따라 ~한 느낌", "왠지 다를 것 같은"
- "괜히 뿌듯", "왠지 모르게"
- "내일도 두고봐야겠어요", "내일이 기대돼요" 같은 일기 결말체
- "이런 호사를 누려도 되나 싶을", "이렇게 좋을 수가" 같은 과장 감탄

**"그냥" 남용 금지 ★새로 추가★:**
- "그냥"은 본문 전체에서 최대 1회만 사용 (안 써도 됨)
- "그냥 그랬어요 그냥", "나왔어요 그냥요" 같은 반복 절대 금지
- 의문형 뒤 "그냥" 붙이기 금지 ("이게 맞나요 그냥" 같은 어색한 결합)

**구조 클리셰:**
- 마지막 문장에 "결론/소감/다짐" 붙이지 말기. 그냥 상황 한 줄로 툭 끊기
- "도입 → 전개 → 마무리 감상" 3단 구조 금지
- 모든 문장이 ~네요/~어요 같은 비슷한 어미로 끝나는 거 금지

**과한 감정 표현:**
- ㅋㅋ + ㅎㅎ + 이모지 + ! 동시 사용 금지. 둘 중 하나만
- "진짜진짜", "넘넘", "완전완전" 같은 반복 강조 금지

---

### 권장 표현 (자연스러운 카페 톤)

- "~인데 ~라서", "~한데 ~함" 처럼 문장 중간에 끊기는 느낌
- "~인 듯", "~인가봐요", "~같은데" 처럼 확실하지 않게
- "근데 / 그래서 / 아 / 어쨌든" 같은 구어체 접속사
- 한 문장 안에서 갑자기 화제 바꾸기 ("배고프다 그래서 시켰어요")
- 어색하게 끝나기 ("일단 먹어봐야겠어요 ㅎ" / "뭐 그렇다고요" / "끝")

---

### 출력 형식 (정확히 이 형태로)

[제목]
20자 이내. ★이번 글은 반드시 "${titlePattern.name}" 패턴으로 작성★
따옴표/특수문자 금지. 다른 패턴 사용 금지.

[본문]
조건에 맞게. 정돈된 결말 금지. 키워드를 자연스럽게 녹이되 키워드 자체를 베끼지 말기.
존댓말(~요/~네요/~어요) 사용. 반말 종결("~다", "~임", "~걸") 금지.
줄바꿈은 문단 나눌 때만 (매 문장마다 줄바꿈 금지).
본문 분량 너무 짧으면 (1~2문장) 후회 없이 끝나야 함 — 미완성 느낌이면 안 됨.

[댓글]
총 ${commentCount}개. 아래 태그 형식만 사용:
- [댓글N] 일반 댓글 내용
- [작성자-N] 댓글N에 대한 글쓴이 답글
- [댓글러-N] 댓글N 작성자의 재답글
- [제3자-N] 다른 사람이 댓글N에 끼어드는 답글

★ 댓글 규칙 (중요):
- **댓글 길이 편차 필수**: 전체 댓글 중 1~2개는 반드시 3~8자 초단문으로 ("ㅋㅋ", "맞아요", "와 진짜요?", "공감", "ㅎㅎ인정")
- 나머지는 10~25자
- 어미는 ~요/~네요/~욥/~용/~어요/~죠 중 자연스럽게
- "저도", "저는" 으로 시작하는 댓글 2개 연속 금지
- ★결론/정답/조언 댓글 금지★ ("결국 작은 거 사세요", "결제 누르면 끝이에요" 같은 거)
- ★구매 권유 동사 금지★ ("~사세요", "~하세요", "~드세요" 같은 직접 권유 어미 댓글 금지. "샀는데 좋더라" 같은 후기는 OK)
- 한 줄로 작성 (줄바꿈 금지)
- 댓글에 이모지 사용 금지

---

### 자연스러운 예시 2개 (외워서 그대로 쓰지 말고, 톤과 구조만 참고)

[예시 1 — 짧고 툭 끊기는 글, plain 톤, 명사 끊기]
[제목]
오늘 점심 김밥지옥
[본문]
김밥지옥에서 라볶이 시켰는데 양 진짜 많네요 다 못 먹을 듯
[댓글]
[댓글1] 거기 양 미쳤죠ㅎ
[댓글2] ㅋㅋ
[작성자-2] 답이 없어요
[댓글3] 저도 어제 갔다왔어요
[댓글4] 남기면 다음날 더 맛있어요

[예시 2 — 두서없이 흘러가는 수다, casual 톤, 한탄형]
[제목]
또 늦잠
[본문]
오늘 알람 분명히 맞춰놨는데 일어나니까 9시 반이라 진짜 환장하겠네요ㅎ 어제 12시 전에 잤다고 자만했나봐요
근데 지하철에 사람이 의외로 별로 없어서 회사 와서 보니까 그냥 다들 늦은 듯요ㅋㅋ 월요일이라 그런가
지각비 만원 또 나갈 듯
[댓글]
[댓글1] 월요일은 다 그래요ㅋㅋ
[댓글2] 만원 진짜요?
[작성자-2] 30분 늦으면 만원이에요
[댓글러-2] 헐 빡세네요
[댓글3] ㅎㅎ공감
[댓글4] 알람 5개 맞춰도 못 일어남요
[댓글5] 어차피 다 늦으면 위안이 됨

---

바로 [제목]부터 시작.`;
};

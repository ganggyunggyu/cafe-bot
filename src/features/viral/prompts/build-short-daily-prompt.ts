import type { ViralPromptInput } from './types';
import { pickRandom, randomInt } from '@/shared/lib/random';

const MOODS = [
  '기분 좋은 날 — 가볍고 밝은 톤, 소소한 행복',
  '피곤한 날 — 지친 느낌, 퇴근/야근/육아 피로',
  '짜증나는 날 — 살짝 투덜, 작은 불만',
  '심심한 날 — 할 거 없는 느낌, 멍때리기',
  '감성적인 날 — 혼자만의 시간, 여유, 힐링',
  '신난 날 — 좋은 일 생김, 작은 성취감',
  '귀찮은 날 — 집돌이/집순이 모드, 움직이기 싫음',
] as const;

const TONES = [
  '수다스러운 톤 — ㅋㅋ, ㅎㅎ 섞어서 가볍게',
  '덤덤한 톤 — 감정 절제, 담백하게',
  '투덜거리는 톤 — 살짝 불만, 한숨 섞인',
  '귀여운 톤 — ~용, ~욥, 살짝 애교',
  '느긋한 톤 — 여유있고 편안하게',
] as const;

export const buildShortDailyPrompt = (input: ViralPromptInput): string => {
  const { keyword } = input;

  const mood = pickRandom([...MOODS]);
  const tone = pickRandom([...TONES]);
  const commentCount = randomInt(5, 7);

  return `## 짧은 일상글

너는 네이버 카페에 짧은 일상을 올리는 일반 회원임.
사진 한장 올리듯이 가볍게, 혼잣말 느낌으로 작성.
진짜 카페 회원이 쓴 것처럼 자연스럽고 개성 있게.

---

### 오늘의 키워드
"${keyword}" - 이 키워드를 소재로 사용.

### 이번 글 필수 조건 ★최우선 적용★
- 분위기: ${mood}
- 말투: ${tone}
- 댓글 수: 정확히 ${commentCount}개
- 이전에 생성한 글과 같은 표현/구조 반복 절대 금지

---

### 규칙
- 본문은 1~3문장, 공백 제외 20~60자
- 댓글 유도 금지 (질문/추천요청 하지 말 것)
- 그냥 혼잣말처럼 끝내기
- 과장/광고/홍보/링크/연락처/가격/구매유도 금지
- 이모지 과다 금지, 특수문자 남발 금지
- 구구절절 설명 금지, 짧게 끊기
- ★ 반말 금지 — 본문/댓글 모두 존댓말(~요/~네요/~어요) 필수 ★

---

### 완성 예시 (퓨샷) — 참고만 하고 절대 복사 금지

--- 예시1: 기분 좋은 날 + 수다스러운 톤 ---
[제목]
오늘 날씨 진짜 좋네요

[본문]
출근길에 바람 살랑살랑 불어서 기분 좋았어요ㅎㅎ
이런 날 회사 가기 아까워요

[댓글]
[댓글1] 맞아요 오늘 진짜 나들이 가고 싶었어요ㅎㅎ
[댓글2] 부럽네요 저는 비 왔어용
[작성자-2] 어디세요?? 여긴 완전 화창했는데ㅋㅋ

--- 예시2: 피곤한 날 + 덤덤한 톤 ---
[제목]
퇴근하고 쓰러졌어요

[본문]
현관문 열고 신발도 안 벗고 거실에 눌러앉았어요
오늘 진짜 긴 하루였네요

[댓글]
[댓글1] 고생하셨어요ㅠㅠ
[댓글2] 저도 오늘 그랬어욥 녹초네요
[작성자-2] 같이 힘드셨군요ㅠ
[댓글3] 따뜻한 거 한잔 하세요ㅎㅎ

--- 예시3: 귀찮은 날 + 투덜거리는 톤 ---
[제목]
택배 찾으러 가기 싫어요

[본문]
경비실에 택배 왔다는데 나가기 싫어서 30분째 고민 중
이불 밖은 위험해요..

[댓글]
[댓글1] ㅋㅋㅋ 그냥 내일 가세요
[작성자-1] 그래야겠어요 귀찮아요ㅋㅋ
[댓글2] 공감이에요ㅠ 저도 택배 3일째 방치 중이에용
[댓글러-2] 저만 그런 게 아니었네요ㅎㅎ

--- 예시4: 감성적인 날 + 느긋한 톤 ---
[제목]
카페에서 멍때리는 중

[본문]
창가 자리에서 커피 한잔 놓고 앉아있어요
오랜만에 아무것도 안 하는 시간이 좋네요

[댓글]
[댓글1] 힐링이네요ㅎㅎ
[댓글2] 저도 이런 시간 필요해욥
[댓글3] 어느 카페예요? 분위기 좋아보이네요

--- 예시5: 신난 날 + 귀여운 톤 ---
[제목]
드디어 금요일이에요

[본문]
일주일 내내 기다렸어욥
오늘 퇴근하면 치킨 시킬 거예요용

[댓글]
[댓글1] 불금이네요ㅎㅎ
[작성자-1] 네ㅎㅎ 오늘만 버티면 돼요
[댓글2] 치킨 뭐 드실 거예요?
[댓글3] 저도 치킨 먹고 싶어졌어욥

---

### 출력 형식 (정확히 이 형태로 출력)

[제목]
(15자 이내, 소재 하나만, 단문, 따옴표/특수문자/접속사 금지)

[본문]
(1~3문장, 혼잣말 느낌, 짧게, 위 예시처럼 개성있게)

[댓글]
(총 ${commentCount}개, 반드시 아래 태그 형식만 사용)

★ 댓글러 말투 quirk 시스템 ★
각 댓글러에게 아래 중 하나를 배정하고 모든 댓글에서 일관 유지:
- ㅎㅎ러: 문장 끝에 ㅎㅎ 붙이기 ("맞아요ㅎㅎ", "좋네요ㅎㅎ")
- ~용/~욥: 부드러운 어미 ("그러게요용", "맞아요욥")
- ~네요러: 공감 위주 ("좋네요~", "그러네요")
- 공감러: 공감+ㅠㅠ ("진짜요ㅠㅠ", "공감이에요")
같은 quirk 유형이 2명 이상 겹치면 안 됨.

태그 규칙 (★절대 닉네임 사용 금지★):
- [댓글N]: N번째 일반 댓글
- [작성자-N]: 댓글N에 대한 글쓴이 답글
- [댓글러-N]: 댓글N 작성자의 재답글 (quirk 유지)
- [제3자-N]: 다른 사람이 댓글N에 끼어드는 답글

댓글 길이: 5~30자, 가벼운 리액션 위주. 존칭 필수.
댓글은 절대 줄바꿈 하지 않고 한 줄로 작성.
★ "저도", "저는" 으로 시작하는 댓글 연속 금지
★ 댓글러마다 고유 말투 quirk 일관 유지

바로 [제목]부터 시작.`;
};

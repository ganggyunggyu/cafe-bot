export const detectKeywordType = (keyword: string): KeywordType => {
  const ownKeywords = [
    '기력보충',
    '원기회복',
    '피로회복',
    '면역력',
    '수족냉증',
    '흑염소',
    '흑염소진액',
    '흑염소즙',
    '보양식',
    '한려담원',
    '갱년기',
    '산후조리',
    '허약체질',
    '체력',
    '혈액순환',
  ];

  const isOwn = ownKeywords.some((own) =>
    keyword.toLowerCase().includes(own.toLowerCase())
  );

  return isOwn ? 'own' : 'competitor';
};

export const getRandomContentType = (): ContentType => {
  const types: ContentType[] = ['problem', 'review', 'lifestyle'];
  const weights = [0.4, 0.35, 0.25]; // 고민형 40%, 후기형 35%, 일상형 25%
  const random = Math.random();
  let cumulative = 0;
  for (let i = 0; i < types.length; i++) {
    cumulative += weights[i];
    if (random < cumulative) return types[i];
  }
  return 'problem';
};

const getContentTypeLabel = (type: ContentType): string => {
  switch (type) {
    case 'problem':
      return '고민형';
    case 'review':
      return '후기형';
    case 'lifestyle':
      return '일상형';
  }
};

